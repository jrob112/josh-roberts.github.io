function range(start, end, step = 0) {
  let result = [];
  if (start < end && step > 0) {
    for (let i = start; i <= end; i + step) {
      result.push(i);
    }
  } else if (start > end && step < 0) {
      for (let i = start; i >= end; i - step) {
        result.push(i);
      }
  }
  return result;
}

